/*
 
    01, 任何非 0 都为真, 只有 0 才为假
    02, ==, != 优先级 低于 <, <=, >, >=
    03, 关系运算符的结合方向为从左到右
    04, 关系运算符优先级 低于 算术运算符
 
 */

#include <stdio.h>

int main() {

    int i = 5 != 4 + 2 * 7 > 3 == 10;
    
    printf("i: %d\n", i);
    printf("%d; %d\n", !1, ~0b011);
    
    return 0;
}


